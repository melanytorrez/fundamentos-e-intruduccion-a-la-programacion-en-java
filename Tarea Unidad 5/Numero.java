public class Numero{
    private int digito1;
    private int digito2;
    private int digito3;
    public Numero (int digito1,int digito2, int digito3){
        this.digito1=digito1;
        this.digito2=digito2;
        this.digito3=digito3;
    }
    public int getDigito1(){
        return digito1;
    }
    public int getDigito2(){
        return digito2;
    }
    public int getDigito3(){
        return digito3;
    }
}
